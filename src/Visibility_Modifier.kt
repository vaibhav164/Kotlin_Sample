 * visibility modifier refers to the accessibility of the declared entity in a program below are examples

 public -> by default every entity is public
 this entity will be visible to any client who can see the declaring class

 private => visible inside the class only

 protected->  visible inside the class and its derived classes(subclasses)

 internal -> visible to any client inside the module that can see the declaring class

 If we override the protected member in a derived class without specifying its visibility,  its visibility can also be
